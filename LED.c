/******************************************************************************/
/* InitLEDs
 *
 * The function initializes the LEDs.										  */
/******************************************************************************/
void InitLEDs(void)
{
	unsigned char i;

	for(i= 0;i<20;i++)
	{
		LED_Green(ON);
		MSC_DelayUS(100000);
		LED_Green(OFF);
		MSC_DelayUS(100000);
	}
	LED_Green(OFF);
}

/******************************************************************************/
/* LED_Green
 *
 * The function controls the GreenLED on the launchpad.						  */
/******************************************************************************/
void LED_Green(unsigned char state)
{
	if(state)
	{
		gioPORTB->DSET = (1L << GREEN_LED_GPIO);
	}
	else
	{
		gioPORTB->DCLR = (1L << GREEN_LED_GPIO);
	}
}


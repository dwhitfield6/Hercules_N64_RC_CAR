/******************************************************************************/
/* InitTest
 *
 * The function initializes the test points.								  */
/******************************************************************************/
void InitTest(void)
{
	TEST_Set1(OFF);
	TEST_Set2(OFF);
}

/******************************************************************************/
/* TEST_Toggle1
 *
 * The function toggles test point 1.										  */
/******************************************************************************/
void TEST_Toggle1(void)
{
	if(gioPORTA->DIN & (1L << TEST_POINT_1))
	{
		TEST_Set1(OFF);
	}
	else
	{
		TEST_Set1(ON);
	}
}

/******************************************************************************/
/* TEST_Toggle2
 *
 * The function toggles test point 2.										  */
/******************************************************************************/
void TEST_Toggle2(void)
{
	if(gioPORTA->DIN & (1L << TEST_POINT_2))
	{
		TEST_Set2(OFF);
	}
	else
	{
		TEST_Set2(ON);
	}
}

/******************************************************************************/
/* TEST_Set1
 *
 * The function sets test point 1.											  */
/******************************************************************************/
void TEST_Set1(unsigned char state)
{
	if(state)
	{
		gioPORTA->DSET = (1L << TEST_POINT_1);
	}
	else
	{
		gioPORTA->DCLR = (1L << TEST_POINT_1);
	}
}

/******************************************************************************/
/* TEST_Set2
 *
 * The function sets test point 2.											  */
/******************************************************************************/
void TEST_Set2(unsigned char state)
{
	if(state)
	{
		gioPORTA->DSET = (1L << TEST_POINT_2);
	}
	else
	{
		gioPORTA->DCLR = (1L << TEST_POINT_2);
	}
}

